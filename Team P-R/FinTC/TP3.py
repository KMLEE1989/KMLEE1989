stock_name = '카카오증권'
if stock_name == "키움증권":
    print("키움 증권이 맞네요!")

elif stock_name != "카카오증권":
    print("카카오 증권이 맞네요!")

else:
    print("위에 if문 조건들이 다 안맞네요!")

